import { execFileSync } from "node:child_process";
import { randomBytes } from "node:crypto";
import { chmodSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const workspace = process.cwd();
const target = resolve(workspace, ".env.local");
const marker = "# Generated by npm run env:local. Local development only.";
const requestedOrigin =
  process.env.DITELE_LOCAL_APP_ORIGIN ?? "http://127.0.0.1:3100";
const origin = new URL(requestedOrigin);

if (
  origin.protocol !== "http:" ||
  !["127.0.0.1", "localhost"].includes(origin.hostname)
) {
  throw new Error("DITELE_LOCAL_APP_ORIGIN must be a loopback HTTP URL.");
}

let authenticationRateLimitHmacKey;
if (existsSync(target)) {
  const existing = readFileSync(target, "utf8");
  if (!existing.startsWith(marker)) {
    throw new Error(
      ".env.local was not generated by this script; preserving it unchanged.",
    );
  }
  const preservedKey = existing.match(
    /^DITELE_AUTH_RATE_LIMIT_HMAC_KEY=([0-9a-f]{64})$/mu,
  )?.[1];
  if (preservedKey) authenticationRateLimitHmacKey = preservedKey;
}

authenticationRateLimitHmacKey ??= randomBytes(32).toString("hex");

const cli = resolve(workspace, "node_modules/.bin/supabase");
let status;
try {
  status = JSON.parse(
    execFileSync(cli, ["status", "-o", "json"], {
      cwd: workspace,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    }),
  );
} catch {
  throw new Error(
    "Local Supabase is unavailable. Run npm run db:start before npm run env:local.",
  );
}

const required = {
  NEXT_PUBLIC_SUPABASE_URL: status.API_URL,
  NEXT_PUBLIC_SUPABASE_ANON_KEY: status.ANON_KEY,
  SUPABASE_SERVICE_ROLE_KEY: status.SERVICE_ROLE_KEY,
};

for (const [name, value] of Object.entries(required)) {
  if (typeof value !== "string" || value.length === 0 || /[\r\n]/u.test(value)) {
    throw new Error(`Local Supabase did not provide a safe ${name} value.`);
  }
}

const contents = [
  marker,
  `NEXT_PUBLIC_SUPABASE_URL=${required.NEXT_PUBLIC_SUPABASE_URL}`,
  `NEXT_PUBLIC_SUPABASE_ANON_KEY=${required.NEXT_PUBLIC_SUPABASE_ANON_KEY}`,
  `SUPABASE_SERVICE_ROLE_KEY=${required.SUPABASE_SERVICE_ROLE_KEY}`,
  `DITELE_AUTH_RATE_LIMIT_HMAC_KEY=${authenticationRateLimitHmacKey}`,
  `DITELE_APP_ORIGIN=${origin.origin}`,
  "DITELE_DATA_MODE=supabase",
  "DITELE_AI_PROVIDER=disabled",
  "DITELE_LAB_PROVIDER=disabled",
  "DITELE_INTEGRATION_PROVIDER=disabled",
  "",
].join("\n");

writeFileSync(target, contents, { encoding: "utf8", mode: 0o600 });
chmodSync(target, 0o600);
console.log(`Configured ${target} for ${origin.origin}; secret values were not printed.`);
